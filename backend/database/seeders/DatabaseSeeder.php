    public function make_proposals_then_choose_one()
    {
        $dates = [2, 3, 4, 5, 6];
        $professionals = PrestadorServico::all();

        foreach (SolicitacaoServico::all() as $request) {
            $proposeCount = rand(3, 10);

            $ids = [];
            while (count($ids) < $proposeCount) {
                $professional = $professionals->random();

                if (!in_array($professional->id, $ids)) {
                    PropostaServico::create([
                        "solicitacao_servico_id" => $request->id,
                        "prestador_servico_id" => $professional->id,
                        "descricao_proposta" => $this->quotes[array_rand($this->quotes)],
                        "valor_servico" => rand(1, 20) * 100,
                        "prazo" => $dates[array_rand($dates)] . " dias"
                    ]);
                    array_push($ids, $professional->id);
                }
            }

            $request->status = 4;
            $request->save();

            $proposals = PropostaServico::query()->where("solicitacao_servico_id", $request->id)->get();
            $proposal = $proposals->random();
            $proposal->status = 1;
            $proposal->save();

            PropostaServico::query()
                ->where("solicitacao_servico_id", $request->id)
                ->where("id", "!=", $proposal->id)
                ->update(["status" => 2]);

            AvaliacaoCliente::create([
                "solicitacao_servico_id" => $request->id,
                "cliente_id" => $request->cliente_id,
                "titulo" => "Feedback para o cliente",
                "mensagem" => $this->quotes[array_rand($this->quotes)],
                "nota_cliente" => rand(1, 5),
                "nota_sistema" => rand(1, 5)
            ]);

            AvaliacaoPrestador::create([
                "solicitacao_servico_id" => $request->id,
                "prestador_servico_id" => $professional->id,
                "titulo" => "Feedback para o prestador de serviço",
                "mensagem" => $this->quotes[array_rand($this->quotes)],
                "nota_prestador" => rand(1, 5),
                "nota_sistema" => rand(1, 5)
            ]);
        }
    }
